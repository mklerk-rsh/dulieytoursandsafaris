    waMessage += `⏱️ *Duration:* ${duration}%0A`;
    waMessage += `💰 *Price from:* ${priceFrom}%0A`;
    waMessage += `🏨 *Accommodation:* ${accommodation}%0A%0A`;

    waMessage += `👥 *GUEST DETAILS:*%0A`;
    waMessage += `• Name: ${name}%0A`;
    waMessage += `• Phone/WhatsApp: ${phone}%0A`;
    waMessage += `• Email: ${email}%0A`;
    waMessage += `• Country: ${country}%0A`;
    waMessage += `• Adults: ${adults}%0A`;
    if (parseInt(children) > 0) waMessage += `• Children (0-12): ${children}%0A`;
    waMessage += `• Travel Date: ${travelDate}%0A%0A`;

    waMessage += `✅ *WHAT'S INCLUDED:*%0A${inclusions}%0A%0A`;

    if (message) {
        waMessage += `💬 *SPECIAL REQUESTS / QUESTIONS:*%0A${message}%0A%0A`;
    }

    waMessage += `🌟 Thank you for choosing *Duliey Tours & Safaris*!%0A`;
    waMessage += `We will contact you shortly to confirm your epic Kenyan adventure 🦒✨`;

    // Your correct WhatsApp number
    const whatsappNumber = "2547029131281";

    // Open WhatsApp
    const whatsappURL = `https://wa.me/${whatsappNumber}?text=${waMessage}`;
    window.open(whatsappURL, '_blank');

    // Visual feedback
    const submitBtn = form.querySelector('button[type="submit"]');
    const originalText = submitBtn.innerHTML;
    submitBtn.innerHTML = '✅ Request Sent via WhatsApp!';
    submitBtn.disabled = true;
    submitBtn.classList.remove('btn-warning');
    submitBtn.classList.add('btn-success');

    // Reset button after 4 seconds
    setTimeout(() => {
        submitBtn.innerHTML = originalText;
        submitBtn.disabled = false;
        submitBtn.classList.remove('btn-success');
        submitBtn.classList.add('btn-warning');
    }, 4000);
}
