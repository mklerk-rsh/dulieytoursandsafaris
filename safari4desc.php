    const message = form.querySelector('textarea[name="message"]').value.trim();

    // Hidden package details
    const packageName = form.querySelector('input[name="package_name"]').value;
    const duration = form.querySelector('input[name="duration"]').value;
    const accommodation = form.querySelector('input[name="accommodation"]').value;
    const priceFrom = form.querySelector('input[name="price_from"]').value;
    const inclusions = form.querySelector('input[name="inclusions"]').value;

    // Validation
    if (!name || !phone || !email || !country || !travelDate) {
        alert("Please fill in all required fields marked with *");
        return;
    }

    // Build professional WhatsApp message
    let waMessage = `*🦁 NEW SAFARI BOOKING REQUEST* %0A%0A`;
